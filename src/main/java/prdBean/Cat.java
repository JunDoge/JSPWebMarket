package prdBean;

public class Cat {
	private int cat_id; 
	private String cat_nm; 
	private String cat_des;
	public int getCat_id() {
		return cat_id;
	}
	public void setCat_id(int cat_id) {
		this.cat_id = cat_id;
	}
	public String getCat_nm() {
		return cat_nm;
	}
	public void setCat_nm(String cat_nm) {
		this.cat_nm = cat_nm;
	}
	public String getCat_des() {
		return cat_des;
	}
	public void setCat_des(String cat_des) {
		this.cat_des = cat_des;
	}

}
